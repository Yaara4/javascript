var  a = 10;
var  b = 3;

console.log("Addition: a + b =", a + b);
console.log("Subtraction: a - b =", a - b);
console.log("Multiplication: a * b =", a * b);
console.log("Division: a / b =", a / b);
console.log("Modulus: a % b =", a % b);
console.log("Exponentiation: a ** b =", a ** b);

var  x = 5;
x++;
console.log("Increment: x++ →", x);

var  y = 5;
y--;
console.log("Decrement: y-- →", y);

console.log("Order of operations: a + b * 2 =", a + b * 2);
console.log("Parentheses change order: (a + b) * 2 =", (a + b) * 2);
console.log("Negative numbers: -a + b =", -a + b);
console.log("Double negatives: -a - (-b) =", -a - (-b));

var  c = 0.1, d = 0.2;
console.log("Floating point: c + d =", c + d);

console.log("Square root using exponent: a ** 0.5 =", a ** 0.5);
console.log("Cube root using exponent: 27 ** (1/3) =", 27 ** (1/3));

var  m = -4, n = -5;
console.log("Multiplying negatives: m * n =", m * n);
console.log("Dividing negatives: m / b =", m / b);

console.log("Complex order: (a + b) * (b + 2) =", (a + b) * (b + 2));
