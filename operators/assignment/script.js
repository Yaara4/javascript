// Basic assignment
let x = 10;
console.log("x =", x); // Output: x = 10

// Addition assignment
x += 5; // equivalent to x = x + 5
console.log("x after += 5:", x); // Output: x after += 5: 15

// Subtraction assignment
x -= 3; // equivalent to x = x - 3
console.log("x after -= 3:", x); // Output: x after -= 3: 12

// Multiplication assignment
x *= 2; // equivalent to x = x * 2
console.log("x after *= 2:", x); // Output: x after *= 2: 24

// Division assignment
x /= 4; // equivalent to x = x / 4
console.log("x after /= 4:", x); // Output: x after /= 4: 6

// Remainder assignment
x %= 4; // equivalent to x = x % 4
console.log("x after %= 4:", x); // Output: x after %= 4: 2

// Exponentiation assignment
x **= 3; // equivalent to x = x ** 3
console.log("x after **= 3:", x); // Output: x after **= 3: 8

